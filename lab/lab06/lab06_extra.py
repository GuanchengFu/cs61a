def vending_machine(snacks):
    """Cycles through sequence of snacks.

    >>> vender = vending_machine(('chips', 'chocolate', 'popcorn'))
    >>> vender()
    'chips'
    >>> vender()
    'chocolate'
    >>> vender()
    'popcorn'
    >>> vender()
    'chips'
    >>> other = vending_machine(('brownie',))
    >>> other()
    'brownie'
    >>> vender()
    'chocolate'
    """
    index = 0
    "*** YOUR CODE HERE ***"
    def f():
        nonlocal index
        temp = index
        if index == len(snacks) - 1:
            index = 0
        else:
            index += 1
        return snacks[temp]
    return f


